# Змінна min має число від 0 до 59.
# Визначити в яку чверть години потрапляє число, відповідно вивести на екран
# одне з значень: «перша», «друга», «третя»б «четверта».

def quarter(min)
    if min >= 0 && min <= 59
        case min
        when 0..15
            puts 'перша'
        when 15..30
            puts 'друга'
        when 30..45
            puts 'третя'
        else
            puts 'четверта'    
        end
    end
end